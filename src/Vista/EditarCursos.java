    private void editarProgramas() {

        ProgramasJpaController control = new ProgramasJpaController();

        this.programa.setNombre(txtNombreEdit.getText());
        this.programa.setDescripcion(txtDescripcionEdit.getText());

        try {

            control.edit(this.programa);

            JOptionPane.showMessageDialog(null, "Cambios Guardados");
        } catch (Exception ex) {
            Logger.getLogger(EditarCursos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void cargarDatosPrograma(Programas programa) {
        //ocultar del usuario
        if (programa != null) {
            txtIDEdit.setText(programa.getIdProgramas().toString());

            txtNombreEdit.setText(programa.getNombre());
            txtDescripcionEdit.setText(programa.getDescripcion());
        }
    }

